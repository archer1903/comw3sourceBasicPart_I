package w3resourceBasicsPart_I;

public class E15_SwapVariables {
    public static void main(String[] args) {

        //swap (takas) two variables

        int a=10, b=15;

        System.out.println(""+a+b);

        int temp;

        temp=a;
        a=b;
        b=temp;

        System.out.println(""+a+b);
















    }
}
